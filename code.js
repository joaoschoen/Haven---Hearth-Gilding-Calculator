function filter_gildables_by_slot(selection){
    let options = []
    for (let i = 0; i < gildables.length; i++) {
        const item = gildables[i];
        for (let j = 0; j < item.slots.length; j++) {
            const slot = item.slots[j];
            if(slot.includes(selection)){
                options.push(item)
            }
        }
    }
    return options
}

